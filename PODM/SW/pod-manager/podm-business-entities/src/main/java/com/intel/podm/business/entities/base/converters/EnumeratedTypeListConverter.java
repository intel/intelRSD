package com.intel.podm.business.entities.base.converters;

import com.intel.podm.common.types.EnumeratedType;
import com.intel.podm.common.types.helpers.EnumeratedTypeListHolder;

import static com.intel.podm.common.types.helpers.EnumeratedTypeListHolder.enumeratedTypeListHolder;

public class EnumeratedTypeListConverter<T extends EnumeratedType> implements PropertyTypeConverter<String, EnumeratedTypeListHolder<T>> {
    private final Class<T> enumType;

    public EnumeratedTypeListConverter(Class<T> enumType) {
        this.enumType = enumType;
    }

    @Override
    public String convertFromMappedType(EnumeratedTypeListHolder<T> enumeratedTypeListHolder) {
        return enumeratedTypeListHolder == null
                ? null
                : enumeratedTypeListHolder.toString();
    }

    @Override
    public EnumeratedTypeListHolder<T> convertToMappedType(String text) {
        return text == null
                ? null
                : enumeratedTypeListHolder(enumType, text);
    }
}
